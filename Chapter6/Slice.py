s = 'Monty Python'
print(s[0:4])   # we can look at any continuous section or the string by using
print(s[6:7])   # the second number is one beyond the second slice(not included)
print(s[6:20])  # if the second number is beyond the end of the string, it stops at the end
print(s[:2])
print(s[8:])
print(s[:])
